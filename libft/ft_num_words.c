#include "libft.h"

int		ft_num_words(char const *s)
{
	int len;

	len = 0;
	while (*s != '\0')
	{
		len++;
		while (*s != '\0' && *s != ' ' && *s != '\t')
			s++;
		while (*s == ' ' || *s == '\t')
			s++;
	}
	return (len);
}
